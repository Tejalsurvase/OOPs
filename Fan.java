class Fan 
{
	String brand ;
	String color ;
	int size ;
	double price ;
	String type ;

	Fan()
	{
		
	}

	public Fan showBrand()
	{
		System.out.println(brand) ;
		return this ;
	}

	public Fan showColor()
	{
		System.out.println(color) ;
		return this ;
	}

	public Fan showSize()
	{
		System.out.println(size) ;
		return this ;
	}

	public Fan showPrice()
	{
		System.out.println(price) ;
		return this ;
	}

	public Fan showType()
	{
		System.out.println(type) ;
		return this ;
	}
}
